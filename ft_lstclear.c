#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*p;

	while (*lst)
	{
		p = ft_lstlast(*lst);
		ft_lstdelone(p);
		free(p->next);
	}
}
